#include "libft.h"

int	ft_min(int *arr, int size)
{
	int	i;
	int	j;

	i = 0;
	j = arr[i];
	while (i < size)
	{
		if (arr[i] < j)
			j = arr[i];
		i++;
	}
	return (j);
}
